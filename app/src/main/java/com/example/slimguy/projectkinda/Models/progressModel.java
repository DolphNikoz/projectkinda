package com.example.slimguy.projectkinda.Models;

public class progressModel{

    String kidnumber,level,kidname,parentname,behavior,behavie,date;

    public progressModel() {
    }

    public String getKidnumber() {
        return kidnumber;
    }

    public void setKidnumber(String kidnumber) {
        this.kidnumber = kidnumber;
    }
    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getKidname() {
        return kidname;
    }

    public void setKidname(String kidname) {
        this.kidname = kidname;
    }

    public String getParentname() {
        return parentname;
    }

    public void setParentname(String parentname) {
        this.parentname = parentname;
    }

    public String getBehavior() {
        return behavior;
    }

    public void setBehavior(String behavior) {
        this.behavior = behavior;
    }

    public String getBehavie() {
        return behavie;
    }

    public void setBehavie(String behavie) {
        this.behavie = behavie;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
